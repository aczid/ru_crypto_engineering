unsigned int round_counter;
key_t 	key;
block_t roundkey;
block_t state;

void rotate_left_61(void){
	/*
	key_t newkey;
	unsigned int bit;
	memset(newkey.bytes, 0x0, 10);
	for(bit = 0; bit < 80; bit++){
		if(test_bit(key,bit)){
			set_bit(newkey,(bit+61)%80);
		}
	}
	memcpy(key.bytes, newkey.bytes, 10);
	*/

	/* Stolen from Zhu/Gong implementation - much faster, of course */
	unsigned char temp[3];
	temp[2] = key.bytes[9];
	temp[1] = key.bytes[8];
	temp[0] = key.bytes[7];
	key.bytes[9] = key.bytes[6] << 5 | key.bytes[7] >> 3;
	key.bytes[8] = key.bytes[5] << 5 | key.bytes[6] >> 3;
	key.bytes[7] = key.bytes[4] << 5 | key.bytes[5] >> 3;
	key.bytes[6] = key.bytes[3] << 5 | key.bytes[4] >> 3;
	key.bytes[5] = key.bytes[2] << 5 | key.bytes[3] >> 3;
	key.bytes[4] = key.bytes[1] << 5 | key.bytes[2] >> 3;
	key.bytes[3] = key.bytes[0] << 5 | key.bytes[1] >> 3;
	key.bytes[2] = temp[2] << 5 | key.bytes[0] >> 3;
	key.bytes[1] = temp[1] << 5 | temp[2] >> 3;
	key.bytes[0] = temp[0] << 5 | temp[1] >> 3;
}

void schedule_key(void){
	/* 3. [k38k37k36k35k34] ^= round_counter - also stolen from Zhu/Gong */
	key.bytes[5] ^= round_counter << 2;

	/* 1. [k79k78..k1k0] = [k18k17..k20k19] */
	rotate_left_61();
	
	/* 2. [k79k78k77k76] = S[k79k78k77k76] */
	/* sbox(high nibble) | low nibble */
	key.bytes[0] = (sbox[key.bytes[0] >> 4] << 4) | (key.bytes[0] & 0xf);
	/* Zhu/Gong eliminated the left shift by making their s-box produce upper nibbles */

	/* Ki = K63K62..K0 = k79k78..k16 */
	memcpy(roundkey.bytes, key.bytes, 8);
}

void addRoundKey(void){
	state.value ^= roundkey.value;
}

void sBoxLayer(void){
	unsigned int i;
	for(i = 0; i < 8; i++){
		/* sbox(high nibble) | sbox(low nibble) */
		state.bytes[i] = (sbox[state.bytes[i] >> 4] << 4) | sbox[(state.bytes[i] & 0xf)];

		/* On my 64-bit Atom, the wide s-box is only marginally faster - what's going on here? */
		/*
		state.bytes[i] = wide_sbox[state.bytes[i]];
		*/
	}
}

void pLayer(void){
	block_t newstate;
	unsigned int bit;
	newstate.value = 0;

	for(bit = 0; bit < 64; bit++){
		if(test_bit(state,bit)){
			/* Kostas figured out the pattern */
			set_bit(newstate,((16 * (bit % 4)) + (bit / 4)));
		}
	}
	state.value = newstate.value;
}

void init(void){
	memcpy(roundkey.bytes, key.bytes, 8);
}

void update(void){
	addRoundKey();
	sBoxLayer();
	pLayer();
	schedule_key();
}

void final(void){
	addRoundKey();
}

void encrypt(void){
	init();
	for(round_counter = 1; round_counter < 32; round_counter++){
		update();
	}
	final();
}

void print_block(block_t block){
	printf("%02x%02x%02x%02x %02x%02x%02x%02x\n",
		block.bytes[0], block.bytes[1], block.bytes[2], block.bytes[3],
		block.bytes[4], block.bytes[5], block.bytes[6], block.bytes[7]
	);
}

void test(int keyval, int input){
	memset(key.bytes, keyval, 10);
	state.value = input;
	printf("Plaintext:  ");
	print_block(state);
	encrypt();
	printf("Ciphertext: ");
	print_block(state);
}

int main(int argc, char* argv[]){
	test(0,0);
	test(-1,0);
	test(0,-1);
	test(-1,-1);

	return 0;
}


