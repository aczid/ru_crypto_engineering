#define test_bit(x,n)	x.bytes[(sizeof(x.bytes)-1)-(n/CHAR_BIT)] &  (1<<(n%CHAR_BIT))
#define set_bit(x,n)	x.bytes[(sizeof(x.bytes)-1)-(n/CHAR_BIT)] |= (1<<(n%CHAR_BIT))

